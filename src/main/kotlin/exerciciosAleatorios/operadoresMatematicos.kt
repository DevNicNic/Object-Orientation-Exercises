package exerciciosAleatorios

fun main() {
    var numero1 = 20
    var numero2 = 10
    println(numero1 + numero2) // soma
    println(numero1 - numero2) // subtração
    println(numero1 / numero2) // divisão
    println(numero1 * numero2) // multiplicação
    println(numero1 % numero2) // resto da divisão
    println(++numero1) //operador de incremento vai sempre acrescentar + 1 ao valor da variavel
    println(--numero2) //operador de decremento subtrai 1 no valor da variavel
}
