fun main() {
    var valor1 = 20
    var valor2 = 10

    println(valor1 == valor2) // operador igual
    println(valor1 < valor2) // operador menor
    println(valor1 > valor2) //operador maior
    println(valor1 <= valor2) //menor ou igual
    println(valor1 >= valor2) //maior ou igual
    println(valor1 != valor2) // diferente

}